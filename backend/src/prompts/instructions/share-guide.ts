/**
 * 分享文案规则
 *
 * 适用于所有包含 share_text 字段的 Prompt
 */
export const SHARE_TEXT_GUIDE = `## share_text 生成规则

格式：引子 + 自我调侃 + 行动号召

1. **引子**（吸引注意）
   - 用问句或悬念开头
   - 如："原来我..."、"怪不得我..."、"终于知道为什么..."

2. **自我调侃**（展示真实感）
   - 适度自嘲，不要太严肃
   - 如："确实有点..."、"被说中了..."、"不愧是我..."

3. **行动号召**（引发互动）
   - 暗示别人也能测
   - 如："你们呢？"、"谁懂？"、"快来看看你的"

示例：
✅ "原来我那么爱操心是月亮处女的锅？朋友们早该告诉我的😂 你们的月亮在哪？"
✅ "怪不得最近老想摆烂，土星正在考验我呢... 谁懂这种感觉？"
✅ "终于知道为什么我总是刀子嘴豆腐心了，火星天秤实锤！不信你也来测"

❌ "我的太阳在狮子座"（太平淡）
❌ "今日运势75分"（没有个性）
❌ "测了一下星盘很准"（没有吸引力）`;

/**
 * 不同模块的分享文案风格指南
 */
export const SHARE_STYLE_BY_MODULE: Record<string, string> = {
  natal: '侧重自我发现的惊喜感，如"原来我是这样的人"',
  daily: '侧重当日能量的共鸣感，如"今天的我需要..."',
  synastry: '侧重关系洞察的趣味性，如"我俩这配置绝了"',
  cbt: '侧重情绪觉察的共鸣感，如"原来我不是一个人"',
  annual: '侧重年度主题的期待感，如"2026年我要..."',
};

/**
 * 获取模块特定的分享风格提示
 */
export function getShareStyleHint(module: string): string {
  return SHARE_STYLE_BY_MODULE[module] || '侧重个人洞察的惊喜感';
}

